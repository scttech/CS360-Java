package com.example.app_poll.classes;

public class ClassItem {
    final int id;
    final String name;
    final String section;
    final String semester;
    final String year;
    public ClassItem(int id, String name, String section, String semester, String year) {
        this.id = id;
        this.name = name;
        this.section = section;
        this.semester = semester;
        this.year = year;
    }
}
